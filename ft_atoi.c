#include "libft.h"

int	ft_atoi(const char *str)
{
	int	count;
	long long	result;

	count = 0;
	result = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			count++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	if (count % 2 == 1)
		return (-result);
	return (result);
}
